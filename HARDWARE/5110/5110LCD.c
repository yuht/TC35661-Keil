// 写一个字符
//****************************************************************************************************
void LCD_WriteChar(unsigned char X,unsigned char Y,unsigned char c)
{
    unsigned char i,j ;
   const  unsigned char *map = &font[0][0];
    c -= 32; //变换
    map += c *( HEIGHT/8*WIDTH);
   
    for(i=0;i<HEIGHT/8;i++)
    {
        LCD_Set_XY(X, Y+i);
        for(j=0;j<WIDTH;j++)
	    LCD_WriteByte(*map++, 1);
    }
}

